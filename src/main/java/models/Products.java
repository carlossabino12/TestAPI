package models;

public class Products {

    public String nome;
    public String preco;
    public String descricao;
    public String quantidade;

    public Products(String nome, String preco, String descricao, String quantidade){
        this.nome = nome;
        this.preco = preco;
        this.descricao = descricao;
        this.quantidade = quantidade;

    }

}
